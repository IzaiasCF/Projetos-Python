# Algoritmo para fazer suco de laranja

laranjas = int(input("Quantas laranjas você tem? "))

if laranjas >= 3:
    print("Cortando as laranjas...")
    print("Espremendo as laranjas no copo...")
    print("Adicionando açúcar a gosto...")
    print("Misturando...")
    print("✅ Suco pronto!")
else:
    print("⚠️ Laranjas insuficientes para fazer o suco!")
